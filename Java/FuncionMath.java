
// Ejercicio en clase, exploracion de la libreria Math

class FuncionMath {
    public static void main(String[] args) {

        int numero1=10,numero2=20;

        System.out.println("Maximo         = "+Math.max(numero1,numero2));
        System.out.println("Minimo         = "+Math.min(numero1,numero2));
        System.out.println("Raiz cuadrada  = "+Math.sqrt(5));
        System.out.println("Valor absoluto = "+Math.abs(-45));
        System.out.println("Random         = "+Math.random());
    }
}
